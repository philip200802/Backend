
const mongoose = require('mongoose');

let customerSchema = mongoose.Schema({
    firstName: { 
        type: String, 
        required: true, 
        trim: true 
    },
    lastName: { 
        type: String, 
        required: true, 
        trim: true 
    },
    email: { 
        type: String, 
        required: true, 
        unique: true,
        lowercase: true 
    },
    password: { 
        type: String, 
        required: true 
    }
}, { timestamps: true });

const Customer = mongoose.model('user', customerSchema);

module.exports = Customer;